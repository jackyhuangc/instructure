public enum LogTypeEnum {

    QUOTAS(0, "余额"),
    POINTS(1, "积分");

    private Integer value;

    private String desc;

    LogTypeEnum(Integer value, String desc) {
        this.value = value;
        this.desc = desc;
    }

    public Integer getValue() {
        return value;
    }

    public String getDesc() {
        return desc;
    }
}
